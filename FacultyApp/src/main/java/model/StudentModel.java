package model;

/**
 *
 * @author milut
 */
public class StudentModel {
    private String name;
    private String email;
    private int year;
    private int semester;

    public StudentModel(String name, String email, int year, int semester) {
        this.name = name;
        this.email = email;
        this.year = year;
        this.semester = semester;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getSemester() {
        return semester;
    }

    public void setSemester(int semester) {
        this.semester = semester;
    }
    
    
}
